    private void v_comprarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_v_comprarMouseClicked
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        Helado helado = null;

        try (HeladoDAOImplementation heladoDAO = new HeladoDAOImplementation(); VentasDAOImplementation ventaDAO = new VentasDAOImplementation()) {

            helado = heladoDAO.obtenerHelado(this.posicion);

            if (helado != null) {
                if (!(helado.getPrecio() >= monedero)) {

                    if (!(helado.getCantidad() > 0)) {
                        v_error.setText("No hay suficiente dinero o no queda de ese helado");
                    } else {
                        heladoDAO.updateCantidad(helado.getCantidad() - 1, this.posicion);
//                        System.out.println(helado.getPosicion() + " cantidad: " + helado.getCantidad());
                        ventaDAO.setVenta(this.posicion);
                        monedero = monedero - helado.getPrecio();
                        v_dinero.setText(monedero + "");
                        v_error.setText("");
                    }

                } else {
                    v_error.setText("No hay suficiente dinero o no queda de ese helado");
                }
            }

        } catch (Exception e) {
            System.out.println(e.getClass() + " " + e.getMessage());
        }

        DefaultTableModel miTabla = (DefaultTableModel) this.v_tabla.getModel();
        miTabla.setNumRows(0);
        Object[] datos = new Object[5];

        try (HeladoDAOImplementation heladoDAO = new HeladoDAOImplementation()) {
            ArrayList<Helado> helados = (ArrayList) heladoDAO.getHelados();

            for (Helado heladito : helados) {
                datos[0] = heladito.getPosicion();
                datos[1] = heladito.getNombre();
                datos[2] = heladito.getTipo();
                datos[3] = heladito.getPrecio();
                datos[4] = heladito.getCantidad();
                miTabla.addRow(datos);
            }

        } catch (Exception e) {
            System.out.println(e.getClass() + " " + e.getMessage());
        }
    }//GEN-LAST:event_v_comprarMouseClicked

