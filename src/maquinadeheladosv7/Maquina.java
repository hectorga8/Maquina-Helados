    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        DefaultTableModel miTabla = (DefaultTableModel) this.v_tabla.getModel();
        Object[] datos = new Object[5];
        try (HeladoDAOImplementation heladoDAO = new HeladoDAOImplementation()) {
            ArrayList<Helado> helados = (ArrayList) heladoDAO.getHelados();

            for (Helado helado : helados) {
                datos[0] = helado.getPosicion();
                datos[1] = helado.getNombre();
                datos[2] = helado.getTipo();
                datos[3] = helado.getPrecio();
                datos[4] = helado.getCantidad();
                miTabla.addRow(datos);
            }

        } catch (Exception e) {
            System.out.println(e.getClass() + " " + e.getMessage());
        }
    }//GEN-LAST:event_formWindowOpened

